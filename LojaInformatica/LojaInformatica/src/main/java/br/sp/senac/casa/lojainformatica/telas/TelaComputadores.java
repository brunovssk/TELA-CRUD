    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        // Dados da interface para um objeto. 
        String marca = txtMarca.getText();
        String hd = txtHD.getText();
        String processador = txtProcessador.getText();

        Computador objComputador = new Computador(marca, hd, processador);  // Aqui precisei criar o construtor na classe Computador e deixar "HD" em min√∫sculo. 

        // Agora iremos passar o objeto para o DB: 
        boolean retornoDB = ComputadorDAO.salvar(objComputador);
        if (retornoDB) {
            JOptionPane.showMessageDialog(rootPane, "Computador cadastrado com sucesso!");

        } else {
            {
                JOptionPane.showMessageDialog(rootPane, "Falha no ao cadastrar computador!");

            }
        }


    }//GEN-LAST:event_btnAdicionarActionPerformed

